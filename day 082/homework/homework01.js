let number1 = parseInt(prompt("Please enter the first number:"));
let number2 = parseInt(prompt("Please enter the second number:"));

if (number1 > number2) {
    console.log("Number 1 is greater than number 2.");
} else if (number1 < number2) {
    console.log("Number 1 is less than number 2.");
} else {
    console.log("Number 1 is equal to number 2.");
}